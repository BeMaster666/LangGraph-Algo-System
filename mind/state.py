"""
AgentState — 唯一信息载体。字段按【归属节点 → 数据方向】分组。

方向标记：
  → 输入  从用户原始信息中提取，未加工
  → 加工  中间节点处理后的产物
  → 交付  最终输出给用户的内容
"""
import operator
from typing import TypedDict, Annotated, Sequence
from langgraph.graph.message import add_messages
from langchain_core.messages import BaseMessage


def _merge_metadata(a: dict, b: dict) -> dict:
    """metadata reducer：浅合并而非替换。任一为 None/空时取另一侧。"""
    a = a or {}
    b = b or {}
    return {**a, **b}


class AgentState(TypedDict):
    # ════════════════════════════════════════════════════════
    # LangGraph 管理 — 基础结构
    # ════════════════════════════════════════════════════════
    messages: Annotated[Sequence[BaseMessage], add_messages]
    """所有历史消息，用于多轮上下文还原。LangGraph 自动维护。"""

    # ════════════════════════════════════════════════════════
    # Extractor — 输入：从用户消息中解析原始信息
    # ════════════════════════════════════════════════════════
    target_id: str
    """用户提到的题号，字符串形式。"""
    source_code: str
    """用户提交的源码文本。"""
    error_message: str
    """用户附带的报错信息。"""
    current_query: str
    """本轮用户的自然语言描述 / 问题。"""
    fact_ledger: Annotated[list[dict], operator.add]
    """每轮事实摘要的累加列表，跨轮追踪用户说了什么。累加器 reducer。"""

    # ════════════════════════════════════════════════════════
    # Summarizer — 加工：为 LLM 节点提供浓缩背景
    # ════════════════════════════════════════════════════════
    long_focus_context: str
    """fact_ledger 拼接为结构化文本，供生成节点做长程上下文。"""

    # ════════════════════════════════════════════════════════
    # Fetcher — 输入：从外部获取题目信息
    # ════════════════════════════════════════════════════════
    problem_detail: str
    """题目的完整描述（本地 / doocs / LeetCode GraphQL）。"""
    default_template: str
    """题目的默认代码模板（函数签名）。"""

    # ════════════════════════════════════════════════════════
    # Diagnosis — 加工：判定回答角色 + 评估信息充分度
    # ════════════════════════════════════════════════════════
    intent: str
    """回答角色：error / teach / generate。决定下游走哪条生成路径。"""
    expert_diagnosis: str
    """分类依据：从用户输入中看到的信号，解释了为什么归为这个 intent。"""
    sufficiency: float
    """信息充分度 [0.0, 1.0]。供 gatekeeper 做放行判定。"""
    gatekeeper_verdict: str
    """决策者判定结果："proceed" / "ask"。"""

    # ════════════════════════════════════════════════════════
    # RAG — 加工：从知识库检索参考解法
    # ════════════════════════════════════════════════════════
    standard_solution: str
    """向量检索到的标准解法 / 知识点，作为生成节点的参考素材。"""

    # ════════════════════════════════════════════════════════
    # Reviewer — 加工：生成修复方案
    # ════════════════════════════════════════════════════════
    fix_suggestion: str
    """针对用户代码错误的修复建议（含代码块）。"""
    complexity_analysis: str
    """修复后的复杂度分析。"""

    # ════════════════════════════════════════════════════════
    # Tutor — 加工：生成教学内容
    # ════════════════════════════════════════════════════════
    tutor_output: str
    """苏格拉底式分步教学文本。"""

    # ════════════════════════════════════════════════════════
    # Generator — 加工：生成完整解法
    # ════════════════════════════════════════════════════════
    generator_output: str
    """最优解法的 Python 代码 + 注释。"""

    # ════════════════════════════════════════════════════════
    # Format / Gatekeeper — 交付：最终输出给用户
    # ════════════════════════════════════════════════════════
    final_report: str
    """Format 按 intent 组装 Markdown 报告；Gatekeeper 在信息不足时直接写入追问文本。这是用户最终看到的内容。"""

    # ════════════════════════════════════════════════════════
    # 所有节点 — 遥测：运行时可观测数据
    # ════════════════════════════════════════════════════════
    metadata: Annotated[dict, _merge_metadata]
    """各节点写入延迟 / 状态 / 置信度。浅合并，永不覆盖。"""
